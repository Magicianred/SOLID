package SingleResponsibilityPrinciple.edited.entity;

public class Pizza {
	public String cesit;
	public String ebat;
	public String fiyat;

	public void setCesit(String cesit) {
		this.cesit = cesit;
	}

	public void setEbat(String ebat) {
		this.ebat = ebat;
	}

	public void setFiyat(String fiyat) {
		this.fiyat = fiyat;
	}

	public String getCesit() {
		return cesit;
	}

	public String getEbat() {
		return ebat;
	}

	public String getFiyat() {
		return fiyat;
	}

}
